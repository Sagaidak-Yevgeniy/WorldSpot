def tr(lang: str, key: str) -> str:
    return TRANSLATIONS.get(lang, TRANSLATIONS["en"]).get(key, key)


def tr_country(lang: str, country: str) -> str:
    return tr(lang, f"country_{country}") if f"country_{country}" in TRANSLATIONS.get(lang, {}) else country


def tr_region(lang: str, region: str) -> str:
    return tr(lang, f"region_{region}") if f"region_{region}" in TRANSLATIONS.get(lang, {}) else region


def tr_achievement(lang: str, aid: str, field: str) -> str:
    return tr(lang, f"ach_{aid}_{field}")
